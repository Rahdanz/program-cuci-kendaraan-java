    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        int baris=table.rowAtPoint(evt.getPoint());
        String noke=table.getValueAt(baris,0).toString();
        nk.setText(noke);
        String namaa=table.getValueAt(baris,1).toString();
        na.setText(namaa);
        String jenk=table.getValueAt(baris,2).toString();
        jk.setSelectedItem(jenk);
        String gol=table.getValueAt(baris,3).toString();
        go.setSelectedItem(gol);
        String jen=table.getValueAt(baris,4).toString();
        je.setText(jen);
        String har=table.getValueAt(baris,5).toString();
        ha.setText(har);
        String dis=table.getValueAt(baris,6).toString();
        di.setText(dis);
        String tots=table.getValueAt(baris,7).toString();
        th.setText(tots);
        String uang=table.getValueAt(baris,8).toString();
        ub.setText(uang);
        String kembali=table.getValueAt(baris,9).toString();
        uk.setText(kembali);
        
        String a=je.getText().toString();
        if(a.equalsIgnoreCase("Cuci Biasa")){
        pc.setSelectedItem("CB");
        }
        else if(a.equalsIgnoreCase("Cuci Full")){
        pc.setSelectedItem("CF");
        }
        else if(a.equalsIgnoreCase("Cuci Snow Full")){
        pc.setSelectedItem("CSF");
        }
    }//GEN-LAST:event_tableMouseClicked

