    private void chActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_chActionPerformed
        // TODO add your handling code here:
        String a,b;
        double hargar,tots;
        double disk=0;
        double tambahan=0;
        a=String.valueOf(jk.getSelectedItem());
        b=String.valueOf(go.getSelectedItem());
        hargar=Double.parseDouble(ha.getText());
        if(a.equalsIgnoreCase("Mobil"))
        {
            tambahan=10000;
        
        }
        else if(a.equalsIgnoreCase("Motor"))
        {
           tambahan=0;
        }
        if(b.equalsIgnoreCase("Member"))
        {
            disk=hargar*0.05;
        }
        else if(b.equalsIgnoreCase("None"))
        {
            disk=0;              
        }
        
        tots=hargar+tambahan-disk;
        String totstal=Double.toString(tots);
        th.setText(totstal);
        
                
    }//GEN-LAST:event_chActionPerformed

